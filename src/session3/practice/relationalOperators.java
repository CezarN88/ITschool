package session3.practice;

public class relationalOperators {
    public static void main(String[] args) {
        int firstNumber = 10;
        int secondNumber = 30;
        System.out.println(firstNumber == secondNumber);
        System.out.println(firstNumber == secondNumber);
        System.out.println(firstNumber == secondNumber);
        System.out.println(firstNumber == secondNumber);
        System.out.println(firstNumber == secondNumber);
    }
}
